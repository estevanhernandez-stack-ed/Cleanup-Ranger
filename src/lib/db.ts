/**
 * Confirms or unconfirms an amenity for a park.
 */
export async function confirmParkAmenity(
  parkId: string,
  amenityLabel: string,
  userId: string
): Promise<void> {
  const parkRef = doc(db, 'parks', parkId);
  const parkSnap = await getDoc(parkRef);
  
  if (!parkSnap.exists()) {
    await setDoc(parkRef, {
      id: parkId,
      name: "Community Park",
      location: { lat: 0, lng: 0 },
      integrity: 100,
      amenities: []
    });
  }
  
  const snap = !parkSnap.exists() ? await getDoc(parkRef) : parkSnap;
  const data = snap.data();
  const amenities: ParkAmenity[] = data?.amenities || [];
  
  const updatedAmenities = amenities.map(amenity => {
    if (amenity.label === amenityLabel) {
      const confirmedBy = [...(amenity.confirmedBy || [])];
      const index = confirmedBy.indexOf(userId);
      if (index > -1) {
        confirmedBy.splice(index, 1); // Remove
      } else {
        confirmedBy.push(userId); // Add
      }
      return { ...amenity, confirmedBy };
    }
    return amenity;
  });
  
  await updateDoc(parkRef, { amenities: updatedAmenities });
}

/**
 * Adds a new suggested amenity to a park.
 */
export async function addParkAmenity(
  parkId: string,
  label: string,
  icon: string,
  userId: string // First confirmer
): Promise<void> {
  const parkRef = doc(db, 'parks', parkId);
  const parkSnap = await getDoc(parkRef);
  
  if (!parkSnap.exists()) {
    await setDoc(parkRef, {
      id: parkId,
      name: "Community Park",
      location: { lat: 0, lng: 0 },
      integrity: 100,
      amenities: []
    });
  }
  
  const snap = !parkSnap.exists() ? await getDoc(parkRef) : parkSnap;
  const data = snap.data();
  const amenities: ParkAmenity[] = data?.amenities || [];
  
  // Check if it already exists
  if (amenities.some(a => a.label.toLowerCase() === label.toLowerCase())) {
    return;
  }
  
  const newAmenity: ParkAmenity = {
    label,
    icon,
    confirmedBy: [userId]
  };
  
  await updateDoc(parkRef, {
    amenities: [...amenities, newAmenity]
  });
}
