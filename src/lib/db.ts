export interface ParkProfile {
  id: string; // Google Place ID
  name: string;
  integrity: number; // 0-100 score
  location: { lat: number; lng: number };
  isCustom?: boolean;
  createdAt?: FieldValue | Date;
  amenities?: ParkAmenity[];
  photoUrl?: string;
}

export interface ParkAmenity {
  label: string;
  icon: string;
  confirmedBy: string[]; // User IDs
}

export interface HazardReport extends HazardAnalysis {
  id?: string;
  parkId: string;
  photoUrl: string;
  createdAt: FieldValue | Date;
  status: 'Open' | 'Resolved';
}

export interface CleanupReport extends CleanupAnalysis {
  id?: string;
  parkId: string;
  photoUrl: string;
  type: string;
  createdAt: FieldValue | Date;
}

export interface Rally {
  id?: string;
  parkId: string;
  parkName: string;
  title: string;
  description: string;
  date: Date | string; // Ideally a Timestamp but simplifying for MVP
  organizer: string; // User ID or Name
  attendees: string[]; // List of User IDs/Names
  hazardId?: string; // Optional link to a specific reported hazard
  smartEquipmentManifest?: string[]; // Tools to bring, generated by AI
  createdAt: FieldValue | Date;
}

export type ParkActivityItem = 
  | (HazardReport & { activityType: 'hazard' })
  | (CleanupReport & { activityType: 'cleanup' })
  | (Rally & { activityType: 'rally' });

/**
 * Uploads a photo to Firebase Storage and returns the public URL.
 */
export async function uploadEvidence(file: File, parkId: string): Promise<string> {
  const timestamp = Date.now();
  const storageRef = ref(storage, `evidence/${parkId}/${timestamp}_${file.name}`);
  
  const snapshot = await uploadBytes(storageRef, file);
  const downloadUrl = await getDownloadURL(snapshot.ref);
  return downloadUrl;
}

/**
 * Submits a new Hazard Report to Firestore and updates the park's integrity score.
 */
export async function submitHazardReport(
  parkId: string, 
  parkName: string, 
  location: { lat: number; lng: number }, 
  file: File, 
  analysis: HazardAnalysis
): Promise<HazardReport> {
  // 1. Upload photo
  const photoUrl = await uploadEvidence(file, parkId);

  // 2. Prepare report document
  const reportData = {
    ...analysis,
    parkId,
    photoUrl,
    status: 'Open',
    createdAt: serverTimestamp()
  };

  // 3. Save to "hazards" collection
  const hazardsRef = collection(db, 'hazards');
  const docRef = await addDoc(hazardsRef, reportData);

  // 4. Update the park's integrity score based on the severity
  await decreaseParkIntegrity(parkId, parkName, location, analysis.severity);

  return { ...reportData, id: docRef.id } as HazardReport;
}

/**
 * Decreases a park's integrity score based on the severity of a reported hazard.
 * If the park doesn't exist in our DB yet, it creates it starting at 100 before the deduction.
 */
async function decreaseParkIntegrity(
  parkId: string, 
  parkName: string, 
  location: { lat: number; lng: number }, 
  severity: string
) {
  const parkRef = doc(db, 'parks', parkId);
  const parkSnap = await getDoc(parkRef);

  let currentIntegrity = 100;

  if (!parkSnap.exists()) {
    // Create new park baseline if it doesn't exist
    await setDoc(parkRef, {
      id: parkId,
      name: parkName,
      location,
      integrity: 100
    });
  } else {
    currentIntegrity = parkSnap.data().integrity;
  }

  // Deduct points based on severity
  let penalty = 0;
  if (severity === 'Attention Needed') penalty = 5;
  if (severity === 'Tools Required') penalty = 15;
  if (severity === 'Hazardous/Biohazard') penalty = 30;

  const newIntegrity = Math.max(0, currentIntegrity - penalty);

  await updateDoc(parkRef, {
    integrity: newIntegrity
  });
}

/**
 * Gets all active hazard reports for a specific park.
 */
export async function getParkHazards(parkId: string): Promise<HazardReport[]> {
  const hazardsRef = collection(db, 'hazards');
  const q = query(
    hazardsRef, 
    where('parkId', '==', parkId),
    where('status', '==', 'Open'),
    orderBy('createdAt', 'desc')
  );

  const querySnapshot = await getDocs(q);
  return querySnapshot.docs.map(doc => ({ id: doc.id, ...doc.data() } as HazardReport));
}

/**
 * Gets a combined reverse-chronological activity feed (hazards & cleanups) for a specific park.
 */
export async function getParkActivity(parkId: string): Promise<ParkActivityItem[]> {
  const hazardsRef = collection(db, 'hazards');
  const cleanupsRef = collection(db, 'cleanups');
  const ralliesRef = collection(db, 'rallies');

  const qHazards = query(hazardsRef, where('parkId', '==', parkId), orderBy('createdAt', 'desc'));
  const qCleanups = query(cleanupsRef, where('parkId', '==', parkId), orderBy('createdAt', 'desc'));
  const qRallies = query(ralliesRef, where('parkId', '==', parkId), orderBy('createdAt', 'desc'));

  const [hazardsSnap, cleanupsSnap, ralliesSnap] = await Promise.all([
    getDocs(qHazards), 
    getDocs(qCleanups),
    getDocs(qRallies)
  ]);

  const activities: ParkActivityItem[] = [
    ...hazardsSnap.docs.map(d => ({ ...(d.data() as HazardReport), id: d.id, activityType: 'hazard' as const })),
    ...cleanupsSnap.docs.map(d => ({ ...(d.data() as CleanupReport), id: d.id, activityType: 'cleanup' as const })),
    ...ralliesSnap.docs.map(d => ({ ...(d.data() as Rally), id: d.id, activityType: 'rally' as const }))
  ];

  // Sort descending by timestamp
  activities.sort((a, b) => {
    const aTime = (a.createdAt as { toMillis?: () => number })?.toMillis?.() 
      || (a.createdAt instanceof Date ? a.createdAt.getTime() : Date.now());
    const bTime = (b.createdAt as { toMillis?: () => number })?.toMillis?.() 
      || (b.createdAt instanceof Date ? b.createdAt.getTime() : Date.now());
    return bTime - aTime;
  });

  return activities;
}

