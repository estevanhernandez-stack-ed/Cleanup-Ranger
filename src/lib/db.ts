import { collection, addDoc, getDocs, query, where, orderBy, updateDoc, doc, setDoc, getDoc, serverTimestamp, type FieldValue } from 'firebase/firestore';
import { ref, uploadBytes, getDownloadURL } from 'firebase/storage';
import { db, storage } from './firebase';
import type { HazardAnalysis, CleanupAnalysis } from './gemini';
import { SQUAD_MEMBERS } from './dummyData';
